package ToupleAndToupleStream;

public class Touple {
    private String betsOn;
    private int placed;
    public Touple(String betsOn,int placed){
        this.betsOn=betsOn;
        this.placed=placed;
    }
    public String getBetsOn() {
        return betsOn;
    }

    public int getPlaced() {
        return placed;
    }
}
